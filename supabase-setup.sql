-- TCL营销战区签到系统 - Supabase数据库初始化脚本
-- 在 Supabase SQL Editor 中运行此脚本

-- 1. 人员表
CREATE TABLE IF NOT EXISTS persons (
  id SERIAL PRIMARY KEY,
  no TEXT UNIQUE NOT NULL,
  name TEXT NOT NULL,
  l6 TEXT,
  l7 TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. 签到记录表
CREATE TABLE IF NOT EXISTS records (
  id SERIAL PRIMARY KEY,
  person_id INTEGER REFERENCES persons(id) ON DELETE CASCADE,
  checkin_time TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(person_id)
);

-- 3. 启用行级安全（允许匿名访问，适合内部工具）
ALTER TABLE persons ENABLE ROW LEVEL SECURITY;
ALTER TABLE records ENABLE ROW LEVEL SECURITY;

-- 4. 创建公开访问策略（任何人可读写，适合内部签到场景）
CREATE POLICY IF NOT EXISTS "Allow anonymous select persons" ON persons FOR SELECT USING (true);
CREATE POLICY IF NOT EXISTS "Allow anonymous insert persons" ON persons FOR INSERT WITH CHECK (true);
CREATE POLICY IF NOT EXISTS "Allow anonymous update persons" ON persons FOR UPDATE USING (true);
CREATE POLICY IF NOT EXISTS "Allow anonymous select records" ON records FOR SELECT USING (true);
CREATE POLICY IF NOT EXISTS "Allow anonymous insert records" ON records FOR INSERT WITH CHECK (true);
CREATE POLICY IF NOT EXISTS "Allow anonymous delete records" ON records FOR DELETE USING (true);

